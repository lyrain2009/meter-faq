/** 获取所有FAQ */
export function getAllFaqs(): Faq[] {
  return faqs;
}

/** 根据ID获取FAQ */
export function getFaqById(id: string): Faq | undefined {
  return faqs.find((faq) => faq.id === id);
}

/** 根据分类获取FAQ */
export function getFaqsByCategory(category: string): Faq[] {
  if (category === 'all') return faqs;
  return faqs.filter((faq) => faq.category === category);
}

/** 搜索FAQ */
export function searchFaqs(keyword: string): Faq[] {
  const lowerKeyword = keyword.toLowerCase();
  return faqs.filter(
    (faq) =>
      faq.question.toLowerCase().includes(lowerKeyword) ||
      faq.answer.toLowerCase().includes(lowerKeyword) ||
      faq.tags.some((tag) => tag.toLowerCase().includes(lowerKeyword))
  );
}

/** 获取热门FAQ */
export function getHotFaqs(limit: number = 5): Faq[] {
  return faqs
    .filter((faq) => faq.isHot)
    .sort((a, b) => (b.views || 0) - (a.views || 0))
    .slice(0, limit);
}

/** 获取相关FAQ */
export function getRelatedFaqs(currentId: string, limit: number = 3): Faq[] {
  const currentFaq = getFaqById(currentId);
  if (!currentFaq) return [];

  return faqs
    .filter(
      (faq) =>
        faq.id !== currentId &&
        (faq.category === currentFaq.category ||
          faq.tags.some((tag) => currentFaq.tags.includes(tag)))
    )
    .slice(0, limit);
}
