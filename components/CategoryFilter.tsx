'use client';

import { FaqCategory, CATEGORY_INFO, CategoryInfo } from '@/types/faq';

interface CategoryFilterProps {
  selected: FaqCategory | 'all';
  onChange: (category: FaqCategory | 'all') => void;
}

const ALL_CATEGORY: CategoryInfo = {
  id: 'all' as FaqCategory,
  name: '全部',
  icon: 'Grid',
  description: '查看所有问题',
};

export function CategoryFilter({ selected, onChange }: CategoryFilterProps) {
  const categories = [ALL_CATEGORY, ...Object.values(CATEGORY_INFO)];

  return (
    <div className="flex flex-wrap gap-2">
      {categories.map((category) => (
        <button
          key={category.id}
          onClick={() => onChange(category.id as FaqCategory | 'all')}
          className={`px-4 py-2 rounded-lg text-sm font-medium transition-all duration-200
            ${
              selected === category.id
                ? 'bg-blue-600 text-white shadow-md'
                : 'bg-white text-gray-600 hover:bg-gray-50 border border-gray-200'
            }`}
        >
          <span className="flex items-center gap-2">
            <CategoryIcon name={category.icon} />
            {category.name}
          </span>
        </button>
      ))}
    </div>
  );
}

